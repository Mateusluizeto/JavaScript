 // parametros
const sum = function(number1, number2){ /* Podemos criar function dentro de variaveis */
 return number1 + number2 // Retorna o  valor da soma
} 

// valores que vão ser usados para executar a função de soma    
let n1 = 11 
let n2 = 22

console.log(`O número 1 é ${n1}`)
console.log(`O número 2 é ${n2}`)
console.log(`A soma dos número é ${sum(n1, n2)}`) // executa a função
