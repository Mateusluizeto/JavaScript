//Operadores Aritiméticos

 // multiplicação ( * )
 console.log(3.2 * 5.5)

 // divisão ( / )
 console.log(12 / 2)

 // soma ( + )
 console.log(34 + 67)

 // subtração ( - )
 console.log(34 - 23)
  
 // resto de divisão ( % )
 console.log(11 % 3)

 // incremento ( ++ )
 let incremento = 0
 incremento++ // Vai adcionar mais um
 console.log(incremento) 

 // decremento ( -- )
 let decremento = 2
 decremento-- // Vai tirar um
 console.log(decremento)

 // exponencial ( ** )
 console.log(3 ** 2)
 

