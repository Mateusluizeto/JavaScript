// Operadores Lógicos (logical operators)

// - 2 valores booleanos, quando verificados, resultara em verdadeiro ou falso

let pao = true
let queijo = false

// AND &&
console.log(pao && queijo)  // Os dois tem que ser positivo paraa dar true

// OR ||
console.log(pao || queijo) // Um dos dois tem que ser positivo para dar true

// NOT !
console.log(! queijo) // Se ele é verdadeiro vai se tronar falso e se ele for falso vai se tornar verdadeiro